package zadatak2;

public abstract class Koljeno extends Carstvo {

	private String nazivKoljena;
	private Red red;

	public String getNazivKoljena() {
		return nazivKoljena;
	}

	public void setNazivKoljena(String nazivKoljena) {
		this.nazivKoljena = nazivKoljena;
	}

	public Red getRed() {
		return red;
	}

	public void setRed(Red red) {
		this.red = red;
	}
}